package com.biz.hello;

/*
 * JAVA 관점에서의 class(클래스)
 * 1. 어떤 일을 컴퓨터를 통해서 처리하고 자 할때
 *      할일들의 리스트를 모아놓은 파일
 * 2. 컴퓨터에게 어떤 일들을 처리하도록 지시하고자할때
 *     명령어들을 나열해서 명령문을 만들고 
 *     명령문들을 (어떤순서에 의해) 나열하고
 *     그 리스트들을 한 파일에 모아 놓은 것(이것을 클래스라고한다)
 */
public class HelloEx_01 {
	
	/*
	 * JAVA 관점에서의 method(메서드)
	 * 1. 구체적으로 할일들을 지정한 명령 그룹 
	 */
	public static void main(String[] args) {
		
		// 대한민국을 콘솔에 출력해서 보여줘라
		System.out.println("대한민국");
		// 우리나라을 콘솔에 출력해서 보여줘라
		System.out.println("우리나라");
		// "Republic of KOREA"을 콘솔에 출력해서 보여줘라
		System.out.println("Republic of Korea");
	
		System.out.println("대한민국");
		System.out.println("우리나라");
		System.out.println("Republic of Korea");
		
		System.out.println("대한민국");
		System.out.println("우리나라");
		System.out.println("Republic of Korea");
		
		
	}
	
	
}
